package br.com.diebold.partsrequest.data.api.response;

public class FilialResponse {
    private int id;
    private String nome;

    public FilialResponse() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
